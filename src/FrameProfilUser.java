    private void loadDataUser() {
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "SELECT * FROM users WHERE id_user = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, Session.id_user); // ambil dari session
            java.sql.ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                inputNama.setText(rs.getString("nama"));
                inputEmail.setText(rs.getString("email"));
                inputNomorPonsel.setText(rs.getString("nomor"));
                inputKataSandi.setText(rs.getString("password"));
                konfrKataSandi.setText(rs.getString("password"));
            }

            rs.close();
            ps.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data profil: " + e.getMessage());
            e.printStackTrace();
        }
    }

