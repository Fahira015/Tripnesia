    /**
     * Handler saat tombol Ubah ditekan.
     * Memeriksa semua field input, memastikan kata sandi dan konfirmasi cocok,
     * lalu memperbarui data pengguna di database.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        //ambil data dari form
        String nama = inputNama.getText();
        String email = inputEmail.getText();
        String noPonsel = inputNomorPonsel.getText();
        String password = inputKataSandi.getText();
        String konfirmasi = konfrKataSandi.getText();

        //validasi, semua field harus diisi
        if (nama.isEmpty() || email.isEmpty() || noPonsel.isEmpty() || password.isEmpty() || konfirmasi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seluruh field harus diisi!");
            return;
        }

        //validasi, password dan konfirmasi cocok
        if (!password.equals(konfirmasi)) {
            JOptionPane.showMessageDialog(this, "Konfirmasi kata sandi tidak cocok!");
            return;
        }

        //Proses update data ke database
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "UPDATE users SET nama = ?, email = ?, nomor = ?, password = ? WHERE id_user = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nama);
            ps.setString(2, email);
            ps.setString(3, noPonsel);
            ps.setString(4, password);
            ps.setInt(5, Session.id_user);

            int result = ps.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Profil berhasil diperbarui!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui profil.");
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat update data: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    
