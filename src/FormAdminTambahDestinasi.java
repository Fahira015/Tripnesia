    /**
     * Handler untuk tombol Tambah destinasi.
     * Memvalidasi input, menyimpan gambar ke folder, dan menyimpan data ke database.
     * 
     * @param evt Event action yang dipicu oleh tombol
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        // Mengambil data dari form
        String nama = jTextField1.getText();
        String alamat = jTextField2.getText();
        String deskripsi = jTextArea1.getText();
        String harga = jTextField3.getText();
        String jamBuka = jTextField4.getText();
        String kategori = jComboBox2.getSelectedItem().toString();
        
        // Validasi: pastikan semua field terisi
        if (nama.isEmpty() || alamat.isEmpty() || deskripsi.isEmpty() || harga.isEmpty() || jamBuka.isEmpty() || fileGambar == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Semua field harus diisi dan gambar harus dipilih!");
            return;
        }
        
        // Validasi: pastikan kategori sudah dipilih
        if (kategori.equals("Pilih Kategori")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih kategori destinasi!");
            return;
        }

        try {
            Connection conn = Koneksi.getConnection();
            String namaFile = fileGambar.getName();
            // Membuat folder images jika belum ada
            File destFolder = new File("images");
            if (!destFolder.exists()) {
                destFolder.mkdirs(); // bikin folder kalo belum ada
            }
            File destFile = new File(destFolder, namaFile);

            // Menyalin file gambar ke folder images
            Files.copy(fileGambar.toPath(), destFile.toPath(), StandardCopyOption.REPLACE_EXISTING);

            // Query SQL untuk insert data destinasi
            String sql = "INSERT INTO destinasi (nama, alamat, deskripsi, harga, jambuka, kategori ,gambar) VALUES (?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, nama);
            stmt.setString(2, alamat);
            stmt.setString(3, deskripsi);
            stmt.setInt(4, Integer.parseInt(harga));
            stmt.setString(5, jamBuka);
            stmt.setString(6, kategori); 
            stmt.setString(7, namaFile);

            stmt.executeUpdate(); //Eksekusi query

            javax.swing.JOptionPane.showMessageDialog(this, "Destinasi berhasil ditambahkan!");

            // Reset form
            jTextField1.setText("");
            jTextField2.setText("");
            jTextArea1.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jComboBox2.setSelectedIndex(0);
            lblPreview.setIcon(null);
            fileGambar = null;

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi kesalahan saat menyimpan data ke database:\n" + e.getMessage(),
                "SQL Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi kesalahan saat menyimpan gambar:\n" + e.getMessage(),
                "File Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

